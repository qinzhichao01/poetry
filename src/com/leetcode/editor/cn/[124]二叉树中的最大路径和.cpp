/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class Solution
{
private:
    int _res_max_sum = INT_MIN;

public:
    int maxPathSum(TreeNode *root)
    {
        getMax(root);
        return _res_max_sum;
    }
private:
    int getMax(TreeNode *root)
    {
        if (nullptr == root)
        {
            return 0;
        }

        // å·¦å³ä¸¤è¾¹çš„æœ€å¤§å€¼åªæœ‰åœ¨å¤§äº0çš„æƒ…å†µä¸‹æ‰æœ‰æ„ä¹‰
        int left_max = max(getMax(root->left), 0);
        int right_max = max(getMax(root->right), 0);
        int temp = left_max + right_max + root->val;
        _res_max_sum = max(_res_max_sum, temp);

        // è¿™å„¿å› ä¸ºé¢˜ç›®ä¸­ç»™å‡ºçš„æ˜¯ä¸€è·¯å¾„ä¸­åŒä¸€èŠ‚ç‚¹ä¸èƒ½å‡ºç°ä¸¤æ¬¡
        return root->val + max(left_max, right_max);
    }
};
