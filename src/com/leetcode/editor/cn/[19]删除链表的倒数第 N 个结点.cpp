/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode() : val(0), next(nullptr) {}
 *     ListNode(int x) : val(x), next(nullptr) {}
 *     ListNode(int x, ListNode *next) : val(x), next(next) {}
 * };
 */
class Solution {
public:
    ListNode* removeNthFromEnd(ListNode* head, int n) {
        if(!head)
        {
            return head;
        }

        // åŠ ä¸€ä¸ªå¤´èŠ‚ç‚¹
        ListNode *Head = new ListNode;
        Head->next = head;

        auto pHead = Head;
        int sum = 0;
        while(pHead)
        {
            sum++;
            pHead = pHead->next;
            if(n == sum)
            {
                break;
            }
        }

        if(!pHead)
        {
            return nullptr;
        }

        if(sum != n)
        {
            return nullptr;
        }

        auto pCurrent = head;
        auto pBehind = Head;
        while(pHead->next)
        {
            pHead = pHead->next;
            pCurrent = pCurrent->next;
            pBehind = pBehind->next;
        }

        pBehind->next = pCurrent->next;
        delete pCurrent;
        pCurrent = nullptr;
        head = Head->next;
        delete Head;
        Head = nullptr;
        return head;
    }
};
